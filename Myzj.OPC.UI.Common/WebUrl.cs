using System;
using System.Collections.Generic;
using System.Configuration;
using System.Linq;
using System.Text;

namespace Myzj.OPC.UI.Common
{
    /// <summary>
    /// 公共链接
    /// </summary>
    public static class WebUrl
    {
        /// <summary>
        /// WebApp链接
        /// </summary>
        /// <returns></returns>
        public static string WebAppUrl()
        {
            return ConfigurationSettings.AppSettings["WebAppUrl"];
        }

        /// <summary>
        /// 图片主路径
        /// </summary>
        /// <returns></returns>
        public static string ImgUrl()
        {
            return ConfigurationSettings.AppSettings["ImgUrl"];
        }
    }
}
